#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int slen;

	i = 0;
	while (s[i])
		i++;
	slen = i;
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)s + i);
		i--;
	}
	return (0);
}
