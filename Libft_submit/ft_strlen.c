#include "libft.h"

size_t		ft_strlen(const char *s)
{
	const char	*ss;
	size_t		ret;

	ss = s;
	ret = 0;
	while (*ss != '\0')
	{
		ss++;
		ret++;
	}
	return (ret);
}
